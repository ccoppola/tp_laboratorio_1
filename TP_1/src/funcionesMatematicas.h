#ifndef FUNCIONESMATEMATICAS_H_
#define FUNCIONESMATEMATICAS_H_


/** \fn float Sumar(float numeroUno, float numeroDos)
 * \brief Recibe dos numeros por parametro y realiza la suma de los mismos.
 * \param numeroUno primer numero que recibe
 * \param numeroDos segundo numero que recibe
 * \return Devuelve el resultado de la suma.
 * */
float Sumar(float numeroUno, float numeroDos);

/** \fn float Restar(float numeroUno, float numeroDos)
 * \brief Recibe dos numeros por parametro y realiza la resta de los mismos.
 * \param numeroUno primer numero que recibe
 * \param numeroDos segundo numero que recibe
 * \return Devuelve el resultado de la resta.
 * */
float Restar(float numeroUno, float numeroDos);

/** \fn float Multiplicar(float numeroUno, float numeroDos)
 * \brief Recibe dos numeros por parametro y realiza la multiplicacion de los mismos.
 * \param numeroUno primer numero que recibe
 * \param numeroDos segundo numero que recibe
 * \return Devuelve el resultado de la multiplicacion.
 * */
float Multiplicar(float numeroUno, float numeroDos);

/** \fn float Dividir (float numeroUno, float numeroDos)
 * \brief Recibe dos numeros por parametro y realiza la division de los mismos.
 * \param numeroUno primer numero que recibe
 * \param numeroDos segundo numero que recibe
 * \return Devuelve el resultado de la division.
 * */
float Dividir (float numeroUno, float numeroDos);

/** \fn double Factorial(int numero)
 * \brief Recibe dos numeros por parametro y realiza el factorial de los mismos.
 * \param numeroUno primer numero que recibe
 * \param numeroDos segundo numero que recibe
 * \return Devuelve el resultado del factorial.
 * */
double Factorial(int numero);

#endif /* FUNCIONESMATEMATICAS_H_ */

